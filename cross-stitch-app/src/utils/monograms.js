// Gerar monograma profissional
export function generateMonogram(text, style = 'classic', color = '#000000') {
  const letters = text.toUpperCase().split('');
  const alphabet = CLASSIC_ALPHABET;
  
  if (letters.length === 0) return { pixels: {}, width: 0, height: 0 };
  
  // Calcular dimensões
  const letterWidth = 5;
  const letterHeight = 7;
  const spacing = 1;
  const totalWidth = letters.length * letterWidth + (letters.length - 1) * spacing;
  
  const pixels = {};
  let currentX = 0;
  
  letters.forEach((letter, index) => {
    if (alphabet[letter]) {
      const pattern = alphabet[letter];
      
      for (let y = 0; y < pattern.length; y++) {
        for (let x = 0; x < pattern[y].length; x++) {
          if (pattern[y][x] === 1) {
            pixels[`${currentX + x},${y}`] = color;
          }
        }
      }
    }
    
    currentX += letterWidth + spacing;
  });
  
  return {
    pixels,
    width: totalWidth,
    height: letterHeight
  };
}

